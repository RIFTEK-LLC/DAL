uint32_t	dal_string_length(const char* str, uint32_t maxLen)
{
	char*		ptr		= (char*)str;
	uint32_t	result	= 0;
	while ((*ptr != 0x00) && (maxLen > 0))
	{	++ptr;
		--maxLen;
		++result;
	};
	return result;
}
