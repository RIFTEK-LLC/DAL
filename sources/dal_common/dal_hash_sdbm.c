#include "dal_common.h"


//===============================================================================================================================
uint32_t	dal_hash_sdbm(const char* str, uint32_t maxLen)
{
	uint32_t	hash	= 0;

	while ((*str) && (maxLen > 0))
	{
		hash	= (*str++) + (hash << 6) + (hash << 16) - hash;
		maxLen--;
	}

	return hash;
}
